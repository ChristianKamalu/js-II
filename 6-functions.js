//expression
let addTwo = function(a,b){
    return a + b;
}

//declaration
function addTwo(a,b){
    return a + b
}

//arrow function 

//explicit return

let addTwo = (a,b) => {
    return a + b;
}
addTwo(2,5)

//implicit return (return is implied)
let addTwo = (a,b) => a + b

addTwo(2,5)